// Math methods and properties

// pi
const pi = Math.PI
console.log(Math.PI)

// returns the integer portion of a float number
console.log(Math.trunc(Math.PI))

// returns the value of a number rounded to the nearest interger
console.log(Math.round(pi))

// returns the value of a number rounded to the next interger
console.log(Math.ceil(pi))

// returns the value of a number rounded to the last interger
console.log(Math.floor(pi))

// power
console.log(Math.pow(2,3))

// returns random number from 0 - 1
console.log(Math.random())